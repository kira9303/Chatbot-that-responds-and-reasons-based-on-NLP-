#Creating GUI with tkinter
import tkinter
from tkinter import *
import pre_usage
from keras.models import load_model
model = load_model('chatbot_model.h5')
import urllib
import webbrowser
#import cx_Freeze


def send():
    msg = EntryBox.get("1.0",'end-1c').strip()
    EntryBox.delete("0.0",END)

    if msg != '':
        ChatLog.config(state=NORMAL)
        ChatLog.insert(END, "You: " + msg + '\n\n')
        ChatLog.config(foreground="#442265", font=("Verdana", 12 ))
        
        prediction = pre_usage.predict_class(msg, model)
        #print(prediction)
        
        new_prediction = pre_usage.simple_class(msg, model)
        #print(new_prediction)
        if(new_prediction == "Google"):
            webbrowser.open('http://google.com')
        if(new_prediction == "Netflix"):
            webbrowser.open('http://netflix.com')
        if(new_prediction == "Whatsapp"):
            webbrowser.open('https://web.whatsapp.com')
            
            

        res = pre_usage.chatbot_response(msg)
        ChatLog.insert(END, "Bot: " + res + '\n\n')

        ChatLog.config(state=DISABLED)
        ChatLog.yview(END)


base = Tk()
base.title("Your friendly neighbourhood Kira!")
base.geometry("400x500")
base.resizable(width=FALSE, height=FALSE)

#Create Chat window
ChatLog = Text(base, bd=0, bg="white", height="8", width="50", font="Arial",)

ChatLog.config(state=DISABLED)

#Bind scrollbar to Chat window
scrollbar = Scrollbar(base, command=ChatLog.yview, cursor="heart")
ChatLog['yscrollcommand'] = scrollbar.set

#Create Button to send message
SendButton = Button(base, font=("Verdana",12,'bold'), text="Send", width="12", height=5,
                    bd=0, bg="#32de97", activebackground="#3c9d9b",fg='#ffffff',
                    command= send )

#Create the box to enter message
EntryBox = Text(base, bd=0, bg="white",width="29", height="5", font="Arial")
#EntryBox.bind("<Return>", send)


#Place all components on the screen
scrollbar.place(x=376,y=6, height=386)
ChatLog.place(x=6,y=6, height=386, width=370)
EntryBox.place(x=128, y=401, height=90, width=265)
SendButton.place(x=6, y=401, height=90)

base.mainloop()